
let li1 = document.getElementById("li1");
let dltbtn1 = document.getElementById("dlt1");

dltbtn1.addEventListener("click",()=>{
    li1.style.display="none";
})

let up1 = document.getElementsByClassName("upper1");

up1[0].addEventListener("click",()=>{
    li1.style.textTransform="uppercase"; 
    li1.style.fontSize= "1.5em";
    li1.style.color= "green"
})

let lower1 = document.getElementsByClassName("lower1");

lower1[0].addEventListener("click",()=>{
    li1.style.textTransform="lowercase";
    li1.style.fontSize = "0.9em";
})


